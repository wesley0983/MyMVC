package com.orddetails.model;

public class OrddetailsVO {
	
	private String ord_no;
	private String pro_no;
	private Integer ord_probonuns;
	private Integer pro_count;
	
	
	public String getOrd_no() {
		return ord_no;
	}
	public void setOrd_no(String ord_no) {
		this.ord_no = ord_no;
	}
	public String getPro_no() {
		return pro_no;
	}
	public void setPro_no(String pro_no) {
		this.pro_no = pro_no;
	}
	public Integer getOrd_probonuns() {
		return ord_probonuns;
	}
	public void setOrd_probonuns(Integer ord_probonuns) {
		this.ord_probonuns = ord_probonuns;
	}
	public Integer getPro_count() {
		return pro_count;
	}
	public void setPro_count(Integer pro_count) {
		this.pro_count = pro_count;
	}
	
	

}
